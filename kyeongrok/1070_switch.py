'''
12, 1, 2 : winter
  3, 4, 5 : spring
  6, 7, 8 : summer
  9, 10, 11 : fall
'''

i = int(input())

if i == 12 or i == 1 or i == 2:
    print('winter')
if i == 3 or i == 4 or i == 5:
    print('spring')
if i == 6 or i == 7 or i == 8:
    print('summer')
if i == 9 or i == 10 or i == 11:
    print('fall')


